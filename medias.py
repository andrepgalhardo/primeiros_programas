primeira = input('Digite a primeira nota: ')

prim = int(primeira)

segunda = input('Digite a segunda nota: ')
seg = int(segunda)

terceira = input('Digite a terceira nota: ')
ter = int(terceira)

quarta = input('Digite a quarta nota: ')
qua = int(quarta)

media = (prim + seg + ter + qua) / 4

print('A média aritmética é: ', media)
